//       Урок 2: Переменные. Примитивные типы данных
public class Variables {
    public static void main(String [] args){

        // целочисленные переменные
        int myInt;

        myInt = 557; // или можно сразу int myInt = 557;

        // вещественные переменные: в Java по умолчанию число с точкой является Double, поэтому чтобы сделать его Float нужно добавить 'f'
        double myDouble = 156.15; // хранит 32 бита информации
        float myFloat = 15.1f; // хранит 64 бита информации

        char c = 'a';
        boolean b = true;
        byte myByte; // 1 байт = 8 бит (-128;128)

    }
}
