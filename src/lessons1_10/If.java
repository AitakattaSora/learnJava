//      Урок 6: Условный оператор if.

package lessons1_10;

public class If {
    public static void main(String[] args) {
        int myInt = 14;

        if(myInt > 20){
            System.out.println("Less than 20");
        } else if (myInt > 10) {    // hint: можно написать только первую скобку и нажать Enter. Вторая добавиться сама
            System.out.println("Greater than 10");
        }
    }
}